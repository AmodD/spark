		<h3>Treadle Pumps</h3>
		<p>Treadle pump is an innovation of CTARA-IIT(B) which is helpful for small scale irrigation.Treadle pump is operated manually.It can fetch water from 23ft depth and can deliver it up to 35ft height. SPARK donated 2 such pumps to farmers in village Aamlon-Peth.Nashik</p>
	<figure class="column has-text-centered">
		<img src="Treadle_pump_1.jpg">
	</figure>
		<ul><strong>Seeds</strong>
			<li>Villagers at Aamlon are very enthusiastic and ready for experiments.</li>
			<li>SPARK handed over 2 different types of seeds of paddy ( जावयाची गुंडी,झिनी – 50Kg each).</li>
			<li>All farmers got yield in good proportion.</li>
			<li>Few farmers got yield of 1 Quintal per 1 Kg which the highest yield in that area.</li>
			<li>SPARK also handed over kitchen-garden kits (seeds of vegetables having high nutrition value) to farmers in Aamlon</li>
